end
function onCreatePost()
    setProperty('CorruptSky.visible', false)
    setProperty('CorruptWall.visible', false)
    setProperty('CorruptDesk.visible', false)
    setProperty('EvilSky.visible', true)
    setProperty('EvilSchool.visible', true)
    setProperty('EvilStreet.visible', true)
    setProperty('EvilTrees.visible', true)
end
