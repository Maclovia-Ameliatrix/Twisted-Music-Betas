    setProperty('cameraSpeed', 10)
	triggerEvent('Camera Follow Pos', 800, 625)
    setProperty('boyfriend.x',1100)
    setProperty('boyfriend.y',525)
    setProperty('gf.x',800)
    setProperty('gf.y',500)
    setProperty('dad.x',100)
    setProperty('dad.y',600)
    setProperty('pico.x',1400)
    setProperty('pico.y',525)
